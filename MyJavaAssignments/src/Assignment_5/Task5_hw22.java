package Assignment_5;

public class Task5_hw22 {
    /*
    write a program that will print numbers from 15 to 5 as ascending.
    Expected output: 15 14 13 12 11 10 9 8 7 6 5
    */

    public static void main(String[] args) {
        for (int i = 15; i>=5 ; i--) {
            System.out.print(i + " ");
        }
    }
}
