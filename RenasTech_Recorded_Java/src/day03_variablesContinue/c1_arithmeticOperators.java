package day03_variablesContinue;

public class c1_arithmeticOperators {
    public static void main(String[] args) {

        //dataType variablename=data;
        byte oscarAge=31;
        byte halilAge=69;
        double libbyAge=29.5;
        double muhammetAge=130.7;

        //plus operators
        System.out.println(oscarAge+halilAge);
        System.out.println(libbyAge+oscarAge);

        // minus operator
        System.out.println(oscarAge-halilAge);
        System.out.println(muhammetAge-libbyAge);

        //multiple operator
        System.out.println(oscarAge*5);
        System.out.println(libbyAge*2);

        //division
        System.out.println(libbyAge/2);
        System.out.println(oscarAge/3);

        System.out.println(5/3);
        System.out.println(5/3.0);
        System.out.println(5.0/3.0);
        System.out.println(5.0/3);

        //reminder
        System.out.println(10%5);
        System.out.println(10%3);
        System.out.println(10/3);
        System.out.println(10/3.0);
        System.out.println(10%3.0);







    }
}
